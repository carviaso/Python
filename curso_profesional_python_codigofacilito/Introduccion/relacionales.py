# Clase 11: Operadores Relacionales

numero_uno = 5
numero_dos = 10
# * Lista de los oepradores *
"""   
    >
    <
    >=
    <=
    ==
    !=
"""

print("\n ** Operadores relacionales **")

resultado = numero_uno > numero_dos

print(f"\t{numero_uno} > {numero_dos} es {resultado}")
print(f"\t{numero_uno} < {numero_dos} es {numero_uno < numero_dos}")
print(f"\t{numero_uno} >= {numero_dos} es {numero_uno >= numero_dos}")
print(f"\t{numero_uno} <= {numero_dos} es {numero_uno <= numero_dos}")
print(f"\t{numero_uno} == {numero_dos} es {numero_uno == numero_dos}")
print(f"\t{numero_uno} != {numero_dos} es {numero_uno != numero_dos}")